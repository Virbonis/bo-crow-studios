package router

import (
	"encoding/json"
	"net/http"

	"github.com/AxionHQ/tsubasa-admin/web/database"
	"github.com/AxionHQ/tsubasa-admin/web/helper"
	"github.com/AxionHQ/tsubasa-admin/web/middleware"
	"github.com/AxionHQ/tsubasa-admin/web/session"
)

func RegisterAPIUserLoginSession() {
	Base.HandleFunc("/user-login-session", middleware.AuthorizationAPIMiddleware(
		listUserLoginSession,
		helper.ALLOWED_TO_SEE_USER_SESSION_LOG)).Methods(http.MethodGet)
	Base.HandleFunc("/user-login-session/pop-up", listPopUpUserLoginSession).Methods(http.MethodGet)
	Base.HandleFunc("/user-login-session", updateUserLoginSession).Methods(http.MethodPut)
}

func listUserLoginSession(w http.ResponseWriter, r *http.Request) {
	userSession, _ := session.GetRouterUserSession(r)
	userInfo := userSession.User
	stampUser := userInfo.Username

	var p struct {
		UserTeam string `schema:"userteam"`
	}
	if err := decoder.Decode(&p, r.URL.Query()); err != nil {
		httpError(w, http.StatusBadRequest, err.Error())
		return
	}

	res, err := database.ListUserLoginSession(p.UserTeam, stampUser)
	if err != nil {
		httpError(w, http.StatusBadRequest, err.Error())
		return
	}

	writeJSON(w, statusSuccess, res)
}

